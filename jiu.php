				<h2>Datos generales</h2>
				<p>Las técnicas básicas incluyen principalmente luxaciones articulares, y además golpes, patadas, rodillazos, esquivas, empujones, proyecciones, derribos, y estrangulamientos. Estas técnicas se originan en métodos de batalla de los bushi (guerreros japoneses clásicos), para hacer frente a otros guerreros samuráis con armadura, (de ahí su énfasis en atacar con luxaciones, lanzamientos y estrangulaciones, más que fomentar el uso de golpes y patadas) sus diferentes apartados técnicos se han desarrollado a lo largo de cerca de dos milenios.</p>

				<p>El jiu-jitsu clásico además de la defensa sin armas, admite emplear otros objetos como armas defensivas u ofensivas, como pueden ser el abanico, el parasol, las cuerdas (hojōjutsu), las monedas y las armas pequeñas de corte y contundentes, como jutte o tridente, tantō, kakushi buki (armas ocultas) e incluso kusarigama, ryofundo kusari o bankokuchoki, que resultan más elaboradas.</p>
